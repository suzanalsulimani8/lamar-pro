let mediaItems   = [];
let mediaFilter  = 'all';
const MEDIA_PAGE_SIZE = 6;
let mediaShowAll = false;

async function loadMediaItems() {
    // أولاً: جرّب localStorage (يشتغل محلياً وعلى السيرفر)
    try {
        const raw = localStorage.getItem('lamar_media_items');
        if (raw) {
            mediaItems = JSON.parse(raw) || [];
            renderMediaGrid();
            return;
        }
    } catch(e) {}

    // ثانياً: حاول قراءة media-data.json (يشتغل على السيرفر)
    try {
        const res  = await fetch('media-data.json?t=' + Date.now());
        const data = await res.json();
        mediaItems = data.items || [];
    } catch(e) {
        mediaItems = [];
    }
    renderMediaGrid();
}

function renderMediaGrid() {
    const grid    = document.getElementById('media-grid');
    const moreBtn = document.getElementById('media-show-more');
    if (!grid) return;

    const filtered = mediaFilter === 'all'
        ? mediaItems
        : mediaItems.filter(i => i.type === mediaFilter);

    if (filtered.length === 0) {
        grid.innerHTML = `<div class="media-empty"><i class="fa-solid fa-inbox"></i><p>لا يوجد محتوى في هذه الفئة</p></div>`;
        if (moreBtn) moreBtn.style.display = 'none';
        return;
    }

    const visible = mediaShowAll ? filtered : filtered.slice(0, MEDIA_PAGE_SIZE);
    const hasMore = filtered.length > MEDIA_PAGE_SIZE;

    grid.innerHTML = visible.map(item => buildMediaCard(item)).join('');

    if (moreBtn) {
        moreBtn.style.display = hasMore ? 'flex' : 'none';
        moreBtn.innerHTML = mediaShowAll
            ? `<i class="fa-solid fa-chevron-up"></i> عرض أقل`
            : `<i class="fa-solid fa-chevron-down"></i> عرض المزيد (${filtered.length - MEDIA_PAGE_SIZE}+)`;
    }
}

function resolveIdbMedia(item) {
    const key = item.image.slice(6);
    if (idbURLCache[key]) { applyIdbSrc(item.id, key, item.type); return; }
    MediaIDB.get(key).then(blob => {
        if (!blob) return;
        idbURLCache[key] = URL.createObjectURL(blob);
        applyIdbSrc(item.id, key, item.type);
    }).catch(() => {});
}

function applyIdbSrc(id, key, type) {
    const el = document.querySelector(`.media-card [data-idbid="${id}"]`);
    if (!el) return;
    const src = idbURLCache[key];
    if (type === 'videos') {
        el.outerHTML = `<video src="${src}" class="media-uploaded-video" controls></video>`;
    } else {
        el.outerHTML = `<img src="${src}" class="media-uploaded-img" alt="">`;
    }
}

function buildMediaCard(item) {
    const typeLabels = { news: 'خبر', events: 'فعالية', videos: 'مقطع' };
    const label = typeLabels[item.type] || item.type;
    const icons = { news: 'fa-newspaper', events: 'fa-calendar-star', videos: 'fa-circle-play' };

    // اجمع كل الصور
    const imgs = (item.images && item.images.length) ? item.images : (item.image ? [item.image] : []);

    let mediaHtml = '';

    if (item.videoUrl) {
        const ytId = extractYoutubeId(item.videoUrl);
        mediaHtml = ytId
            ? `<div class="media-yt-wrap"><iframe src="https://www.youtube.com/embed/${ytId}" allowfullscreen loading="lazy"></iframe></div>`
            : `<div class="media-placeholder-img videos-bg"><i class="fa-solid fa-circle-play"></i></div>`;
    } else if (imgs.length > 0) {
        mediaHtml = buildImageGrid(imgs, item.id, item.title);
    } else {
        mediaHtml = `<div class="media-placeholder-img ${item.type}-bg"><i class="fa-solid ${icons[item.type] || 'fa-image'}"></i></div>`;
    }

    return `<div class="media-card" data-type="${item.type}">
        <div class="media-card-img">
            ${mediaHtml}
            <span class="media-type-badge badge-${item.type}">${label}</span>
        </div>
        <div class="media-card-body">
            <span class="media-date"><i class="fa-regular fa-calendar"></i> ${item.date || ''}</span>
            <h4>${item.title || ''}</h4>
            <p>${item.summary || ''}</p>
        </div>
    </div>`;
}

// بناء شبكة الصور بأسلوب تويتر
function buildImageGrid(imgs, itemId, title) {
    const count = Math.min(imgs.length, 4);
    const extra = imgs.length > 4 ? imgs.length - 4 : 0;

    const imgEl = (src, idx) => {
        const isVideo = src.startsWith('data:video') || /\.(mp4|webm|ogg)$/i.test(src);
        const overlay = (idx === 3 && extra > 0) ? `<div class="gi-more">+${extra}</div>` : '';

        if (src.startsWith('idb://')) {
            const ovAttr = (idx === 3 && extra > 0) ? `data-overlay="+${extra}"` : '';
            resolveIdbInGrid(src, itemId, idx);
            return `<div class="gi" data-idbid="${itemId}_${idx}" ${ovAttr}>
                <i class="fa-solid fa-spinner fa-spin" style="font-size:28px;opacity:.3"></i></div>`;
        }
        if (isVideo) return `<div class="gi" onclick="openLightbox('${itemId}',${idx})">
            <video src="${src}" class="gi-media" muted playsinline></video>
            <i class="fa-solid fa-circle-play gi-play"></i>${overlay}</div>`;
        return `<div class="gi" onclick="openLightbox('${itemId}',${idx})">
            <img src="${src}" class="gi-media" alt="${title}" loading="lazy">${overlay}</div>`;
    };

    const cells = imgs.slice(0, 4).map((src, i) => imgEl(src, i)).join('');
    return `<div class="img-grid count-${count}">${cells}</div>`;
}

function resolveIdbInGrid(src, itemId, idx) {
    const key = src.slice(6);

    const apply = (url, isVideo) => {
        const el = document.querySelector(`[data-idbid="${itemId}_${idx}"]`);
        if (!el) return;
        const overlay = el.dataset.overlay ? `<div class="gi-more">${el.dataset.overlay}</div>` : '';
        if (isVideo) {
            el.innerHTML = `<video src="${url}" class="gi-media" muted playsinline></video>
                            <i class="fa-solid fa-circle-play gi-play"></i>${overlay}`;
        } else {
            el.innerHTML = `<img src="${url}" class="gi-media" alt="">${overlay}`;
        }
        el.onclick = () => openLightbox(itemId, idx);
    };

    if (idbURLCache[key]) {
        setTimeout(() => apply(idbURLCache[key], idbURLCache[key + '_isVid'] || false), 0);
        return;
    }
    MediaIDB.get(key).then(blob => {
        if (!blob) return;
        const isVideo = blob.type.startsWith('video/');
        idbURLCache[key]           = URL.createObjectURL(blob);
        idbURLCache[key + '_isVid'] = isVideo;
        apply(idbURLCache[key], isVideo);
    }).catch(() => {});
}

function extractYoutubeId(url) {
    const match = url.match(/(?:v=|youtu\.be\/|embed\/)([A-Za-z0-9_-]{11})/);
    return match ? match[1] : null;
}

