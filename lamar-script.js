// المتغيرات العامة
const htmlElem = document.documentElement;
const themeToggle = document.getElementById('theme-toggle');
const langToggle = document.getElementById('lang-toggle');

// 1. تطبيق الثيم المحفوظ عند التحميل — الدارك هو الافتراضي دائماً
(function () {
    const saved = localStorage.getItem('lamar-theme') || 'dark';
    htmlElem.setAttribute('data-theme', saved);
    if (themeToggle) {
        themeToggle.querySelector('i').className = saved === 'dark' ? 'fa-solid fa-sun' : 'fa-solid fa-moon';
    }
    const logoSrc = saved === 'light' ? 'img/logo-blue.svg' : 'img/logo-white.png';
    const logoImg = document.getElementById('logo-img');
    const radarLogoImg = document.getElementById('radar-logo-img');
    if (logoImg) logoImg.src = logoSrc;
    if (radarLogoImg) radarLogoImg.src = logoSrc;
})();

// تبديل الثيم عند الضغط
if (themeToggle) {
    themeToggle.addEventListener('click', () => {
        const isDark = htmlElem.getAttribute('data-theme') === 'dark';
        const newTheme = isDark ? 'light' : 'dark';

        htmlElem.setAttribute('data-theme', newTheme);
        localStorage.setItem('lamar-theme', newTheme);
        themeToggle.querySelector('i').className = isDark ? 'fa-solid fa-moon' : 'fa-solid fa-sun';

        const logoSrc = newTheme === 'light' ? 'img/logo-blue.svg' : 'img/logo-white.png';
        const logoImg = document.getElementById('logo-img');
        const radarLogoImg = document.getElementById('radar-logo-img');
        if (logoImg) logoImg.src = logoSrc;
        if (radarLogoImg) radarLogoImg.src = logoSrc;
    });
}

// 2. تبديل اللغة
if (langToggle) {
    langToggle.addEventListener('click', () => {
        const currentLang = htmlElem.getAttribute('lang') === 'ar' ? 'ar' : 'en';
        const newLang = currentLang === 'ar' ? 'en' : 'ar';
        updateLang(newLang);
    });
}

function setText(id, text) {
    const el = document.getElementById(id);
    if (el) el.textContent = text;
}

function setHTML(id, html) {
    const el = document.getElementById(id);
    if (el) el.innerHTML = html;
}

